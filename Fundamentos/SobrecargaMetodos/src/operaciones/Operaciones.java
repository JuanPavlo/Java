
package operaciones;


public class Operaciones {
    public static int sumar(int a, int b) {
        System.out.println("Suma de numeros enteros: ");
        return a+b;
    }
    public static double sumar(double a, double b) {
        System.out.println("Suma de numeros decimales: ");
        return a+b;
    }
}
